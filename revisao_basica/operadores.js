/* Aritméticos */

const num1 = 10
const num2 = 2

// Adição

console.log(num1 + num2)

// Subtração

console.log(num1 - num2)

// Resto

console.log(num1 % num2)
console.log(num2 % num1)

// Incremento

let contador = 10
console.log(++contador)

// Decremento

console.log(--contador)

// Atribuição

contador += num1
console.log(contador)

// NaN - Not a Number ; parseInt (inteiro), parseFloat (flutuante)
let num3 = 10
let num4 = Number('1024')
console.log(num3 + num4)